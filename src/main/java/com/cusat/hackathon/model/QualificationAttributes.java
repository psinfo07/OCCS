package com.cusat.hackathon.model;

public class QualificationAttributes {
	private String qualificationAttributeId;
	private String attributeName;
	/**
	 * @return the qualificationAttributeId
	 */
	public String getQualificationAttributeId() {
		return qualificationAttributeId;
	}
	/**
	 * @param qualificationAttributeId the qualificationAttributeId to set
	 */
	public void setQualificationAttributeId(String qualificationAttributeId) {
		this.qualificationAttributeId = qualificationAttributeId;
	}
	/**
	 * @return the attributeName
	 */
	public String getAttributeName() {
		return attributeName;
	}
	/**
	 * @param attributeName the attributeName to set
	 */
	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}
	
}
